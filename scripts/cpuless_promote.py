def import_base_of(rec_dir: Path) -> str:
    """The python import base for a recovered-corpus DIRECTORY (the emitted
    bodies' package).  Derived, not hardcoded, so a second corpus flavour can
    live beside the pinned one."""
    return ".".join(rec_dir.resolve().relative_to(REPO_ROOT).parts)


def para_of_seg(ir: dict) -> dict[int, str]:
    """NE segment -> its paragraph-base (the CS the IR keys that segment by)."""
    out: dict[int, str] = {}
    for entry, rec in ir["functions"].items():
        out.setdefault(rec["ne_seg"], entry.split(":")[0])
    return out


def manual_para_keys(map_doc: dict, seg_base: dict[int, str]) -> set[str]:
    """The hand-recovered corpus as paragraph-base CS:IP keys — the entries
    CPUless promotion must NOT generate a parallel body for (adaptgen owns
    them)."""
    out: set[str] = set()
    for f in map_doc["functions"]:
        k = f.get("key")
        if not k:
            continue
        seg_s, off = k.split(":")
        base = seg_base.get(int(seg_s))
        if base:
            out.add(f"{base}:{off}".upper())
    return out


def promote_entries(census: dict, manual: set[str],
                    tiers: tuple[str, ...] = ("leaf",)) -> list[str]:
    """(⋃ tiers) ∩ ¬manual — the mechanical-promotion candidates.

    The leaf slice promotes ``leaf ∩ ¬manual``; the calls-only widening adds
    the ``calls-only`` tier, which the dos_re promoter composes BOTTOM-UP over
    the near/far call DAG (its fixpoint promotes a caller only once every
    direct callee already has a CPUless contract).  The manual corpus is always
    excluded — adaptgen owns those bodies ([[manual-recovery-is-authoritative]])
    and they stay literal lifts in graph_cpuless, so a candidate that calls one
    naturally refuses ``call-abi-composition`` (an honest frontier item, not a
    parallel body)."""
    cand: set[str] = set()
    for t in tiers:
        cand |= {k.upper() for k in census["tiers"].get(t, [])}
    return sorted(cand - manual)


def build_plat_farcalls(manifest_path: Path, out_path: Path) -> tuple[int, int, int]:
    """Derive the PLATFORM far-call contracts for the CPUless emitter from the
    win16 import-thunk table.

    SimAnt reaches every KERNEL/USER/GDI/... API through a static
    ``call far THUNK_SEG:slot`` into the import thunk segment (the
    ``manifest.api_slots`` table).  dos_re's plat.farcall composition needs, per
    slot, the pascal callee-cleanup ``argbytes`` -- which is exactly the sum of
    the API's declared pascal argument sizes, the same number the win16 API
    dispatch pops in its ``ret_far`` (win16/api/core.py).  So the contract is a
    win16 fact, read straight off the api registry: NOTHING game-specific about
    SimAnt is encoded here, and dos_re stays boundary-segment-agnostic (the
    THUNK_SEG and the per-slot argbytes are handed in as data).

    A raw (-register) API owns its own return contract (arg_sizes is None) and a
    slot with no implemented entry is a gap: neither gets a contract, so a
    far-call to it REFUSES ``platform-farcall-contract-unknown`` -- an honest
    frontier item, never a guessed arg count.  Returns (thunk_seg, n_contracts,
    n_skipped)."""
    from win16.api.surface import build_registry
    from win16.app import WINFLAGS_NO_FPU

    manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
    thunk_seg = int(manifest["thunk_seg"])
    reg = build_registry(winflags=WINFLAGS_NO_FPU)
    contracts: dict[str, dict] = {}
    skipped = 0
    for key, off in manifest["api_slots"].items():
        mod, ordn = key.rsplit(".", 1)
        entry = reg.entries.get((mod.upper(), int(ordn)))
        if entry is None or entry.arg_sizes is None:
            skipped += 1            # a gap or a raw API: no derivable argbytes
            continue
        contracts[f"{thunk_seg:04X}:{int(off):04X}"] = {
            "argbytes": sum(entry.arg_sizes),
            "cost": 1,              # one VM step per API dispatch (not owns_time)
            "name": f"{mod.upper()}.{ordn}",
        }
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps({
        "_notice": "GENERATED by scripts/cpuless_promote.py from the win16 "
                   "import-thunk table (manifest.api_slots) + the api "
                   "registry's declared pascal arg sizes. Disposable; "
                   "regenerate, do not hand-edit.",
        "thunk_seg": f"{thunk_seg:04X}",
        "contracts": contracts,
    }, indent=1), encoding="utf-8")
    return thunk_seg, len(contracts), skipped


def route_graph(from_dir: Path, graph_dir: Path, adapter_dir: Path,
                promoted: list[str], overrides: list[str] | None = None
                ) -> tuple[int, list[str]]:
    """Materialize the CPUless-routed graph: copy the literal corpus, then for
    every promoted leaf (and every authoritative OVERRIDE) swap its literal
    module for the generated adapter and remap the manifest stem to the
    adapter's ``lifted_CCCC_IIII`` name.  An override adapter is the identity-
    preserving bridge to the hand-recovered body (the unified override-graph
    seam); it occupies the SAME lifted slot a generated twin would.

    Returns (adapters routed, missing-adapter keys)."""
    from dos_re.lift.naming import GraphNaming

    if not (from_dir / "graph_manifest.json").is_file():
        raise SystemExit(f"cpuless_promote: no graph manifest in {from_dir} — "
                         f"run scripts/liftemit.py + scripts/liftlink.py first")
    graph_dir.mkdir(parents=True, exist_ok=True)
    for old in graph_dir.glob("*.py"):
        old.unlink()
    for src in sorted(from_dir.glob("*.py")):
        (graph_dir / src.name).write_bytes(src.read_bytes())
    for extra in ("graph_manifest.json", "boundary_policy.json"):
        if (from_dir / extra).is_file():
            (graph_dir / extra).write_bytes((from_dir / extra).read_bytes())

    naming = GraphNaming.load(graph_dir)
    mapping = dict(naming.mapping)
    routed = 0
    missing: list[str] = []
    for key in list(promoted) + list(overrides or []):
        cs, ip = key.split(":")
        adapter_stem = f"lifted_{cs.lower()}_{ip.lower()}"
        adapter_src = adapter_dir / f"{adapter_stem}.py"
        if not adapter_src.is_file():
            missing.append(key)
            continue
        old_stem = mapping.get(key)
        if old_stem and old_stem != adapter_stem:
            old_path = graph_dir / f"{old_stem}.py"
            if old_path.is_file():
                old_path.unlink()
        (graph_dir / f"{adapter_stem}.py").write_bytes(adapter_src.read_bytes())
        mapping[key] = adapter_stem
        routed += 1
    GraphNaming(mapping).save(graph_dir)
    return routed, missing


def run_lint(rec_dir: Path, import_base: str = IMPORT_BASE) -> int:
    """CPUless recovered-purity wall: the promoted bodies (and the carrier-free
    override bodies) import nothing but sibling recovered modules and the
    authoritative recovered corpus (``simant.recovered`` / ``simant.bridge``,
    both proven VM-free) -- no CPU carrier at any level.

    The override seam widens the wall to the WHOLE recovered corpus: an override
    body binds a bridge view (``simant.bridge.dgroup_view.SelectorBackend``) and
    calls a hand-recovered function (``simant.recovered.*``), so those two
    packages are scanned for purity and admitted as sibling-recovered prefixes.
    They reach neither ``dos_re.cpu`` nor ``simant.lifted`` (checked here), so
    the standalone runtime still never touches a CPU.

    The ``--root`` half is the RUNNER closure (cont.251): every module
    ``scripts/play_cpuless.py`` can import — its own lazy imports followed,
    then module-level edges transitively — must be CPU-free too.  The runtime
    import guard the runner arms only fires on an executed import; this is the
    static proof for the paths a given run does not take."""
    lint = load_dosre_tool("lint_cpuless")
    return lint.main([
        "--repo-root", str(REPO_ROOT),
        "--root", "scripts/play_cpuless.py",
        "--recovered-root", str(rec_dir.relative_to(REPO_ROOT).as_posix()),
        "--recovered-root", "simant/recovered",
        "--recovered-root", "simant/bridge",
        "--recovered-prefix", import_base,
        "--recovered-prefix", "simant.recovered",
        "--recovered-prefix", "simant.bridge",
        "--forbidden-module", "dos_re.cpu",
        "--forbidden-module", "dos_re.cpu386",
        "--forbidden-module", "dos_re.lift.install",
        "--forbidden-module", "dos_re.lift.runtime",
        "--forbidden-module", "dos_re.runtime",
        "--forbidden-module", "simant.lifted",
        "--forbidden-module", "simant.hooks",
        "--forbidden-module", "win16.loader",
        "--forbidden-module", "win16.bootimage",
        "--local-prefix", "dos_re", "--local-prefix", "simant",
        "--local-prefix", "win16",
        "--package-dir", "win16=win16_re/win16",
        "--package-dir", "dos_re=win16_re/dos_re/dos_re",
    ])


def main(argv=None) -> int:
    ap = argparse.ArgumentParser(
        description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--ir", default=str(DEFAULT_IR))
    ap.add_argument("--census", default=str(DEFAULT_CENSUS))
