    ap.add_argument("--manifest", default=str(DEFAULT_MANIFEST),
                    help="the vmless boot manifest (api_slots + thunk_seg) the "
                         "platform far-call contracts are derived from")
    ap.add_argument("--plat-farcalls-out", default=str(DEFAULT_PLAT_FARCALLS),
                    help="where the derived platform far-call contracts are "
                         "written (fed to the dos_re promoter)")
    ap.add_argument("--dyn-evidence", default=str(DEFAULT_DYN_EVIDENCE),
                    help="indirect_sites.json (per-site observed dynamic-target "
                         "evidence from scripts/entry_probe.py): a function with "
                         "a dynamic transfer promotes only when every OBSERVED "
                         "target of its sites is dispatchable (a local jump-table "
                         "landing, a promoted near-return callee, or an owned "
                         "dispatch entry) -- so a framed switch whose arms are "
                         "unpromoted fragments is gated out, never crashing live "
                         "play with UnknownDispatchTarget.  Omit the file to "
                         "promote every dispatch optimistically (unsound).")
    ap.add_argument("--no-absorb-dispatch-arms", action="store_true",
                    help="disable dispatch-arm absorption (reproduce the "
                         "pre-cont.249 slice, where every framed tail-dispatch "
                         "container refused dyn-target-unpromoted)")
    ap.add_argument("--no-plat-farcalls", action="store_true",
                    help="disable platform far-call composition (reproduce the "
                         "pre-plat.farcall calls-only slice)")
    ap.add_argument("--overrides-out", default=str(DEFAULT_OVERRIDES),
                    help="where the authoritative-override contracts are "
                         "written (scripts/overridegen.py output, fed to the "
                         "dos_re promoter --overrides)")
    ap.add_argument("--with-overrides", action="store_true",
                    help="build the UNIFIED OVERRIDE GRAPH: the 166 "
                         "mechanically-closed hand-recovered bodies compose as "
                         "DIRECT carrier-free CPUless overrides at their own "
                         "address (impl = overrides.get(addr, generated[addr])) "
                         "instead of staying literal lifts, so their callers no "
                         "longer refuse contains-call.  Only the overrides "
                         "carrying an EXACT virtual-time contract are seeded "
                         "(cont.248) -- the rest keep their instruction-exact "
                         "generated body -- so the graph stays admissible to "
                         "the byte-exact gate.  Pair it with --graph-dir "
                         "simant/lifted/graph_cpuless_override to keep the "
                         "pinned graph untouched.")
    ap.add_argument("--overrides-any-time", action="store_true",
                    help="seed EVERY routable override, including the ones "
                         "whose per-invocation cost is path-dependent and so "
                         "run at the ISLAND cost (one dispatch step).  Maximum "
                         "manual coverage, but the graph no longer preserves "
                         "the instruction-count timeline the pinned "
                         "cold_nohooks oracle (and the instruction-count-keyed "
                         "demo) require -- cont.247 measured -16 instructions "
                         "at cp0 accumulating to -2.2M by cp31.  NOT "
                         "gate-admissible; use only with a demo re-recorded "
                         "under this exact config.")
    ap.add_argument("--skin", action="store_true",
                    help="build the SKIN corpus (task #53): the manual corpus "
                         "as CPUless skin on the generated skeleton.  Shorthand "
                         "for --with-overrides --overrides-any-time plus the "
                         "skin dirs (simant/native/cpuless_skin, "
                         "simant/lifted/graph_cpuless_skin, a skin census), so "
                         "EVERY routable hand-recovered body composes directly "
                         "while the PINNED production corpus/graph -- the ones "
                         "the instruction-count-keyed byte-exact gate runs on "
                         "-- are not touched.  The skin graph is NOT "
                         "gate-admissible (island costs); it is what "
                         "scripts/play_cpuless.py executes, and that runner has "
                         "no byte-exact oracle keyed on instruction counts.")
    ap.add_argument("--tiers", default="leaf,calls-only",
                    help="comma-separated census tiers to feed the promoter "
                         "(default: leaf,calls-only — the calls-only widening; "
                         "'leaf' alone reproduces the leaf slice). The promoter "
                         "composes bottom-up, so extra tiers only widen the "
                         "reachable set, never weaken the gate.")
    ap.add_argument("--dry-run", action="store_true",
                    help="run the promotion census only; write no files")
    args = ap.parse_args(argv)

    if args.skin:
        # The skin preset only fills in defaults the caller did not override, so
        # `--skin --graph-dir X` still works and nothing is silently redirected.
        args.with_overrides = True
        args.overrides_any_time = True
        if args.recovered_dir == str(DEFAULT_REC_DIR):
            args.recovered_dir = str(SKIN_REC_DIR)
        if args.graph_dir == str(DEFAULT_GRAPH_DIR):
            args.graph_dir = str(SKIN_GRAPH_DIR)
        if args.census_out == str(DEFAULT_CENSUS_OUT):
            args.census_out = str(SKIN_CENSUS_OUT)
        if Path(args.recovered_dir).resolve() == DEFAULT_REC_DIR.resolve():
            raise SystemExit(
                "cpuless_promote --skin: refusing to write the island-cost skin "
                "corpus into the PINNED production corpus "
                f"({DEFAULT_REC_DIR}) -- an override body would clobber the "
                "instruction-exact generated twin the byte-exact gate runs on.")

    ir = json.loads(Path(args.ir).read_text(encoding="utf-8"))
    census = json.loads(Path(args.census).read_text(encoding="utf-8"))
    map_doc = json.loads(Path(args.map).read_text(encoding="utf-8"))

    tiers = tuple(t.strip() for t in args.tiers.split(",") if t.strip())
    seg_base = para_of_seg(ir)
    manual = manual_para_keys(map_doc, seg_base)
    entries = promote_entries(census, manual, tiers)
    cand_all: set[str] = set()
    for t in tiers:
        cand_all |= {k.upper() for k in census["tiers"].get(t, [])}
    tier_sizes = ", ".join(f"{t}={len(census['tiers'].get(t, []))}"
                           for t in tiers)
    print(f"tiers [{tier_sizes}]; manual-recovered (adaptgen owns): "
          f"{len(manual & cand_all)} ∩manual; CPUless-promotion candidates "
          f"(⋃tiers ∩ ¬manual): {len(entries)}")

    rec_dir = Path(args.recovered_dir)
    import_base = import_base_of(rec_dir)
    graph_dir = Path(args.graph_dir)
    adapter_dir = graph_dir / "_adapters"        # staging for lifted_* adapters

    promote = load_dosre_tool("cpuless_promote")
    promote_argv = [
        "--ir", args.ir,
        "--recovered-dir", str(rec_dir),
        "--adapter-dir", str(adapter_dir),
        "--import-base", import_base,
        "--exclude", f"@{args.boundary_heads}",
        "--entries", ",".join(entries),
        "--census-out", args.census_out,
    ]
    # Evidence-gated dynamic dispatch: a promoted function's OBSERVED tail/near
    # dispatch targets must be dispatchable, or it is refused (never promoted to
    # crash live play with UnknownDispatchTarget).  The switch-arm fragments a
    # framed tail dispatch resolves to are unpromoted, so this gates those
    # containers out until the dispatch-cluster composition lands.
    if args.dyn_evidence and Path(args.dyn_evidence).is_file():
        promote_argv += ["--dyn-evidence", str(args.dyn_evidence)]
        # DISPATCH-ARM ABSORPTION: SIMANTW's switch arms (the SIMANTW.SYM
        # `case_XXXX` fragments) are carved as their own IR "functions" because
        # nothing but the jump table reaches them.  They are really ALTERNATE
        # ENTRIES into their container's body -- which is why each refuses
        # `leave-without-enter` standalone and, through the evidence gate,
        # held the whole message-pump dispatch cluster out of composition.
        # dos_re fuses them into their container (proving the overlap
        # byte-identical); the FACTS are the dyn evidence + the IR, both ours.
        if not args.no_absorb_dispatch_arms:
            promote_argv += ["--absorb-dispatch-arms"]
    # PLATFORM far-call composition (plat.farcall): a `call far THUNK_SEG:slot`
    # into the win16 import-thunk table becomes a platform effect, not a refusal
    # -- the gateway to the calls-only sim tier (nearly every non-trivial sim
    # function reaches an API this way).  The THUNK_SEG + per-slot argbytes are
    # a win16 fact (derived from the api registry), handed to the emitter as
    # data; dos_re stays boundary-segment-agnostic.
    if not args.no_plat_farcalls:
        thunk_seg, n_contracts, n_skipped = build_plat_farcalls(
            Path(args.manifest), Path(args.plat_farcalls_out))
        print(f"platform far-call contracts: {n_contracts} thunk slot(s) "
              f"(seg {thunk_seg:04X}); {n_skipped} skipped (raw API / gap -- "
              f"no derivable argbytes)")
        promote_argv += [
            "--plat-far-segs", f"{thunk_seg:04X}",
            "--plat-farcalls", f"@{args.plat_farcalls_out}",
        ]
    # AUTHORITATIVE OVERRIDES (the unified override-graph seam,
    # [[manual-recovery-is-authoritative]]): the mechanically-closed hand-
    # recovered bodies compose as DIRECT carrier-free CPUless overrides at their
    # own address (impl = overrides.get(addr, generated[addr])) -- so a caller
    # of a manual function no longer refuses contains-call.  overridegen emits
    # the contracts here (the bodies are dropped in AFTER --apply, so the
    # promoter's clean regen does not wipe them); the SAME adaptgen classifier
    # picks the routable set, so dos_re stays game-agnostic.
    overrides: list[str] = []
    if args.with_overrides:
        import overridegen
        og_rc = overridegen.main([
            "--ir", args.ir, "--map", args.map,
            "--out", args.overrides_out, "--dry-run",
        ])
        if og_rc != 0:
            return og_rc
        ov_doc = json.loads(
            Path(args.overrides_out).read_text(encoding="utf-8"))["overrides"]
        promote_argv += ["--overrides", f"@{args.overrides_out}"]
        if args.overrides_any_time:
            overrides = sorted(ov_doc)
            print("WARNING: --overrides-any-time seeds ISLAND-cost overrides; "
                  "the graph is NOT admissible to the instruction-count-keyed "
                  "byte-exact gate (cont.247/248).")
        else:
            # Gate-admissible subset: only the overrides whose per-invocation
            # virtual time is EXACT (overridegen derives it from the IR CFG).
            # The dos_re promoter applies the same filter when seeding, so the
            # routed adapter set and the seeded contract set stay identical.
            promote_argv += ["--overrides-time-exact-only"]
            overrides = sorted(
                k for k, v in ov_doc.items()
                if v.get("virtual_time", {}).get("kind", "island") != "island")
            print(f"overrides: {len(overrides)}/{len(ov_doc)} carry an EXACT "
                  f"virtual-time contract and are seeded; the rest stay on "
                  f"their instruction-exact generated body.")
    if args.dry_run:
        print("\n=== promotion census (dry run) ===")
        return promote.main(promote_argv)

    # Clean regen: the recovered package and the adapter staging are disposable.
    if rec_dir.exists():
        shutil.rmtree(rec_dir)
    rec_dir.mkdir(parents=True, exist_ok=True)
    adapter_dir.mkdir(parents=True, exist_ok=True)

    print("\n=== promote (emit recovered bodies + CPU-ABI adapters) ===")
    rc = promote.main(promote_argv + ["--apply"])
    if rc != 0:
        return rc
    # Package marker so the adapters' `from simant.native.cpuless.func_... import`
    # resolves.  Pure (no imports) — the recovered-purity wall covers it.
    (rec_dir / "__init__.py").write_text(
        '"""AUTOGENERATED CPUless recovered corpus (scripts/cpuless_promote.py)'
        " -- disposable; regenerate, do not hand-edit.\"\"\"\n",
        encoding="utf-8", newline="\n")

    # Drop the carrier-free override BODIES into the recovered corpus (AFTER the
    # promoter's clean regen, so they survive); the override ADAPTERS were
    # already emitted by the dos_re promoter (--overrides) into adapter_dir.
    if args.with_overrides:
        import overridegen
        print("\n=== emit carrier-free CPUless override bodies ===")
        og_rc = overridegen.main([
            "--ir", args.ir, "--map", args.map,
            "--bodies-dir", str(rec_dir), "--out", args.overrides_out,
        ] + ([] if args.overrides_any_time else ["--only-time-exact"]))
        if og_rc != 0:
            return og_rc

    promoted = json.loads(
        Path(args.census_out).read_text(encoding="utf-8"))["promotable"]
    print("\n=== route the CPUless-routed graph "
          f"({graph_dir.relative_to(REPO_ROOT).as_posix()}) ===")
    routed, missing = route_graph(Path(args.from_dir), graph_dir, adapter_dir,
                                  promoted, overrides)
    shutil.rmtree(adapter_dir)                    # staging consumed
    if missing:
        raise SystemExit(f"cpuless_promote: {len(missing)} promoted leaf/leaves "
                         f"had no emitted adapter: {missing[:8]}")
    print(f"routed {routed} CPUless adapter(s) into {graph_dir.name}; the rest "
          f"stay literal lifts")

    print("\n=== CPUless recovered-purity wall (lint_cpuless) ===")
    rc = run_lint(rec_dir, import_base)
    if rc != 0:
        print("CPUless wall: FAIL")
        return rc
    print(f"\nCPUless slice complete: {len(promoted)} leaf function(s) promoted "
          f"to pure-Python recovered bodies + CPU-ABI adapters; wall holds.")
    return 0


