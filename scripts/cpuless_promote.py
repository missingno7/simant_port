"""cpuless_promote — promote SIMANTW's cleanest leaf tier to CPUless (M4 slice).

The CPU-detachment stage of the DOS_RE 2.0 adoption
(win16_re/dos_re/docs/dos_re_2.0.md §1 stage 2): for every liftable LEAF
function that has NO authoritative hand-recovered implementation, run dos_re's
``cpuless_promote`` — the strict first-subset gate + emitter — to generate a
pure-Python recovered body (``simant/native/cpuless/func_CCCC_IIII.py``,
computing over ``(mem[, plat], *regs)`` with no CPU carrier) plus a CPU-ABI
adapter that restores the historical machine ABI around it.

THE MANUAL SPLIT ([[manual-recovery-is-authoritative]]).  Where a verified
manual CPUless implementation already exists in ``simant/recovered/``, IT is
authoritative and the M2b adapter generator (scripts/adaptgen.py) wraps it —
CPUless promotion must NEVER generate a parallel body for such a function.  So
this tool promotes ONLY ``leaf ∩ ¬manual``: it reads the promotion census
(artifacts/cpuless_census.json — scripts/../cpuless_census over the recovery
IR) for the leaf tier and the recovered map (simant/facts/recovered_map.json)
for the manual corpus, and restricts the promoter's ``--entries`` to their
difference.  The two toolchains produce ONE implementation per function.

THE GRAPH SEAM.  A generated adapter is a byte-for-byte drop-in for the literal
lift it replaces — same ``(cpu, bb=0)`` signature, same historical RET, same
``owns_time`` virtual-time accounting — so it installs through the identical
``dos_re.lift.install`` machinery (LINKS late-binding + replacement_hooks).
The routed graph is materialized beside the literal graph as
``simant/lifted/graph_cpuless/``: the literal corpus is copied, each promoted
leaf's symbolically-named module is DELETED and its adapter dropped in, and the
graph manifest entry is remapped to the adapter's ``lifted_CCCC_IIII`` stem
(the ``dos_re.lift.naming`` seam every linked caller resolves through).  One
graph, one implementation per entry.

Everything written here is DISPOSABLE autogenerated output (gitignored, like
simant/lifted/graph): delete it and this script reproduces it from the IR +
census + facts.  Runs AFTER liftemit/liftlink (it builds FROM their linked
corpus); never hand-edit a generated body/adapter — fix the emitter or facts
and regenerate.

    python scripts/cpuless_promote.py            # generate + route + lint
    python scripts/cpuless_promote.py --dry-run  # census only, no files
"""
