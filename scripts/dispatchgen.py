"""dispatchgen — mechanically derive SIMANTW's switch-dispatch entry facts.

The VMless-wall closure step for jmp_ind TAIL dispatchers
(win16_re/dos_re/docs/dos_re_2.0.md §1a): a lifted dispatcher body ends at
``jmp word ptr cs:[bx+TABLE]`` by setting CS:IP to the runtime case target and
handing control back to the step dispatch — so under the armed interpreter
poison EVERY possible case target must itself be a census entry with an
installed lifted hook.  This tool derives those targets MECHANICALLY, with the
table bounds PROVEN from the dispatcher's own guard code, and writes them as
evidence-backed recovery facts:

    simant/facts/dispatch_entries.txt   one NE_SEG:OFFSET per distinct case
                                        target (consumed by scripts/irgen.py as
                                        additional census entries)
    simant/facts/code_as_data.txt       the jump-table byte ranges (data inside
                                        code segments: preserved by the boot-
                                        image poison and declared to its audit)

Derivation (MSC C 6/7 switch codegen, three observed guard families — each
site must prove one exactly, else the dispatcher is STOPPED and reported, never
guessed):

    F1  cmp ax,N / ja DEFAULT / shl ax,1 / xchg ax,bx / jmp cs:[bx+T]
        -> N+1 case slots (index compared, then scaled)
    F2  cmp ax,N / jbe +3 / jmp DEFAULT / shl ax,1 / xchg ax,bx / jmp ...
        -> N+1 case slots (inverted guard)
    F3  cmp ax,N / ja DEFAULT / xchg ax,bx / jmp cs:[bx+T]   (no shl)
        -> N even, N/2+1 case slots (the compared value is already the
           scaled BYTE offset — _GBoxFill's pattern-code family)

Table words are read from the loaded image (the same bytes the dispatcher
reads at run time).  Dispatchers whose jmp_ind is NOT the table idiom (the CRT
register-indirect tails: ``jmp [di]``, ``jmp bx``, chkstk's far returns) are
reported: their runtime targets are census entries or emulated-call return
continuations, both already covered by the step dispatch — the runtime
frontier audit (scripts/play_vmless.py --collect-frontier) is the proof.

    python scripts/dispatchgen.py [--ir artifacts/recovery_ir.json] [--check]
"""
from __future__ import annotations

